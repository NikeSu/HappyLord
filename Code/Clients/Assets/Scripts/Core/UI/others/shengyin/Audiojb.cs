using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Audiojb : MonoBehaviour {
    public TweenPosition xgd;
    public TweenPosition bcd;

    public void syk() {
        GameObject.Find("Main Camera").GetComponent<AudioSource>().Play();
    }
    public void syg() {
        GameObject.Find("Main Camera").GetComponent<AudioSource>().Stop();
    }
    public void xgdbf() {
        xgd.PlayForward();
        bcd.PlayForward();
    }
    public void bcdbf() {
        xgd.PlayReverse();
        bcd.PlayReverse();
    }


        
}
